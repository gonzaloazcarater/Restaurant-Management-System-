    //check if the restaurant name exists
    private void txtRestaurantKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRestaurantKeyReleased
        String restaurantName = txtRestaurant.getText().toLowerCase();

        if(!restaurantName.equals("")){
            iconLabel.setVisible(true);
            iconLabel.setIcon(new ImageIcon("src\\Images\\yes.png"));
            iconLabel.setText("");
            checkRestaurantName = 0;
            try{
                Connection con= ConnectionProvider.getCon();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from Restaurant where RestaurantName='"+restaurantName+"'");
                while(rs.next()){
                    checkRestaurantName = 1;
                    iconLabel.setIcon(new ImageIcon("src\\Images\\no.png"));
                    iconLabel.setText("");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }    
        }
        else{
            iconLabel.setVisible(false);
        }
    }//GEN-LAST:event_txtRestaurantKeyReleased
    
