    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String address = txtAddress.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String restaurantName = txtRestaurant.getText().toLowerCase();
        if(restaurantName.equals("")){
            JOptionPane.showMessageDialog(null, "Restaurant name field is required");
        }else if(address.equals("")){
            JOptionPane.showMessageDialog(null, "Address field is required");
        }else if(phoneNumber.equals("")){
            JOptionPane.showMessageDialog(null, "Phone number field is required");
        }
        else if(!phoneNumber.matches(mobileNumberPattern) || phoneNumber.length()!=10){
            JOptionPane.showMessageDialog(null, "Phone number field is invalid"); 
        }else if(checkRestaurantName ==1){
            JOptionPane.showMessageDialog(null, "Restaurant Name already exists");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into Restaurant (RestaurantAddress, RestaurantName, RestaurantPhone) values(?,?,?)");
                ps.setString(1,address);
                ps.setString(2,restaurantName);
                ps.setString(3,phoneNumber);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Restaurant added successfully");
                setVisible(false);
                new AddRestaurant().setVisible(true);
                
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

